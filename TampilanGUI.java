    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        try {
            String nim = txtnim.getText();
            String nama = txtnama.getText();
            int tahunmasuk = Integer.parseInt(txttahun.getText());
            String jenis = cbJenis.getSelectedItem().toString();
            int sks = Integer.parseInt(txtsks.getText());

            // Hitung biaya berdasarkan jenis mahasiswa
            Mahasiswa mhs;
            switch (jenis) {
                case "Reguler":
                    mhs = new MahasiswaReguler(sks);
                    break;
                case "Beasiswa":
                    mhs = new MahasiswaBeasiswa(sks);
                    break;
                case "Internasional":
                    mhs = new MahasiswaInternasional(sks);
                    break;
                default:
                    mhs = new Mahasiswa(sks, jenis);
            }
            double biaya = mhs.hitungBiayaKuliah();

            // Perbaikan penting: tambahkan biaya_kuliah ke query
            Connection conn = KoneksiDB.getConnection();
            String sql = "INSERT INTO mahasiswa (nim, nama, tahunmasuk, jenis_mahasiswa, sks, biaya_kuliah) VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement ps = conn.prepareStatement(sql);

            ps.setString(1, nim);
            ps.setString(2, nama);
            ps.setInt(3, tahunmasuk);
            ps.setString(4, jenis);
            ps.setInt(5, sks);
            ps.setDouble(6, biaya); // ‚Üê WAJIB!! INI YANG BELUM ADA

            ps.executeUpdate();

            tampilData();
            clear();
            JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan, biaya terhitung!");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error Submit: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        if (selectedId == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data dulu!");
            return;
        }

        try {
            String nim = txtnim.getText();
            String nama = txtnama.getText();
            int tahunmasuk = Integer.parseInt(txttahun.getText());
            String jenis = cbJenis.getSelectedItem().toString();
            int sks = Integer.parseInt(txtsks.getText());

            Mahasiswa mhs;
            switch (jenis) {
                case "Reguler":
                    mhs = new MahasiswaReguler(sks);
                    break;
                case "Beasiswa":
                    mhs = new MahasiswaBeasiswa(sks);
                    break;
                case "Internasional":
                    mhs = new MahasiswaInternasional(sks);
                    break;
                default:
                    mhs = new Mahasiswa(sks, jenis);
            }
            double biaya = mhs.hitungBiayaKuliah();

            String sql = "UPDATE mahasiswa SET nim=?, nama=?, tahunmasuk=?, jenis_mahasiswa=?, sks=?, biaya_kuliah=? WHERE id=?";
            Connection conn = KoneksiDB.getConnection();
            PreparedStatement pst = conn.prepareStatement(sql);

            pst.setString(1, nim);
            pst.setString(2, nama);
            pst.setInt(3, tahunmasuk);
            pst.setString(4, jenis);
            pst.setInt(5, sks);
            pst.setDouble(6, biaya);
            pst.setInt(7, selectedId);

            pst.executeUpdate();

            tampilData();
            clear();
            JOptionPane.showMessageDialog(this, "Data berhasil diupdate");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error Update: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int row = tblMahasiswa.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang mau dihapus!");
            return;
        }
        try {
            String sql = "DELETE FROM mahasiswa WHERE id= ?";
            Connection conn = KoneksiDB.getConnection();
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setInt(1, selectedId);
            pst.executeUpdate();

            tampilData();
            clear();

            JOptionPane.showMessageDialog(this, "Data berhasil dihapus");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, " Tahun Masuk harus berupa angka!");
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnHapusActionPerformed

