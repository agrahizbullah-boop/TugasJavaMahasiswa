    private void importCSV() {
        javax.swing.JFileChooser chooser = new javax.swing.JFileChooser();
        chooser.setFileSelectionMode(javax.swing.JFileChooser.FILES_ONLY);

        int result = chooser.showOpenDialog(this);
        if (result == javax.swing.JFileChooser.APPROVE_OPTION) {

            java.io.File file = chooser.getSelectedFile();

            try {
                java.io.BufferedReader br = new java.io.BufferedReader(new java.io.FileReader(file));
                String line;

                // Lewati baris header
                br.readLine();

                Connection conn = KoneksiDB.getConnection();
                String sql = "INSERT INTO mahasiswa (nim, nama, tahunmasuk) VALUES (?, ?, ?)";
                PreparedStatement ps = conn.prepareStatement(sql);

                while ((line = br.readLine()) != null) {
                    String[] data = line.split(",");

                    if (data.length == 3) {
                        ps.setString(1, data[0]);                // nim
                        ps.setString(2, data[1]);                // nama
                        ps.setInt(3, Integer.parseInt(data[2])); // tahun masuk
                        ps.addBatch();
                    }
                }

                ps.executeBatch();
                br.close();

                tampilData();
                JOptionPane.showMessageDialog(this, "Import CSV Berhasil!");

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Gagal import CSV: " + e.getMessage());
            }
        }
    }

