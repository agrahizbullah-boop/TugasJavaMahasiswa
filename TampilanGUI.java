    private void tampilData() {
        DefaultTableModel model = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // semua sel tidak bisa diubah langsung
            }
        };
        model.addColumn("ID");
        model.addColumn("NIM");
        model.addColumn("Nama");
        model.addColumn("Tahun Masuk");
        model.addColumn("Jenis Mahasiswa"); // Kolom baru
        model.addColumn("SKS");
        model.addColumn("Biaya Kuliah");
        // Kolom baru

// load data dari DB
        try (
                Connection conn = KoneksiDB.getConnection(); Statement st = conn.createStatement(); ResultSet rs = st.executeQuery("SELECT id, nim, nama, tahunmasuk, jenis_mahasiswa, sks, biaya_kuliah FROM mahasiswa ORDER BY id")) { // QUERY DIPERBARUI

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id"),
                    rs.getString("nim"),
                    rs.getString("nama"),
                    rs.getInt("tahunmasuk"),
                    rs.getString("jenis_mahasiswa"), // Ambil data jenis_mahasiswa
                    rs.getInt("sks"), // Ambil data sks
                    rs.getInt("biaya_kuliah")
                });
            }

            tblMahasiswa.setModel(model);
            tblMahasiswa.getColumnModel().getColumn(0).setMinWidth(0);
            tblMahasiswa.getColumnModel().getColumn(0).setMaxWidth(0);
            tblMahasiswa.getColumnModel().getColumn(0).setWidth(0);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal load data: " + e.getMessage());
        }
    }

