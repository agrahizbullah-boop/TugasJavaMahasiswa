    private void hitungDanTampilkanBiaya() {
        try {
            // 1. Ambil input SKS dan Jenis
            // Pastikan SKS tidak kosong sebelum parsing
            if (txtsks.getText().isEmpty()) {
                lblBiaya.setText("SKS kosong!");
                return;
            }

            int sks = Integer.parseInt(txtsks.getText());
            String jenis = cbJenis.getSelectedItem().toString();

            Mahasiswa mhs; // Objek Mahasiswa (Polymorphism)

            // 2. Tentukan class turunan mana yang akan dipakai
            switch (jenis) {
                case "Reguler":
                    // Instansiasi Mahasiswa Reguler
                    mhs = new MahasiswaReguler(sks);
                    break;
                case "Beasiswa":
                    // Instansiasi Mahasiswa Beasiswa
                    mhs = new MahasiswaBeasiswa(sks);
                    break;
                case "Internasional":
                    // Instansiasi Mahasiswa Internasional
                    mhs = new MahasiswaInternasional(sks);
                    break;
                default:
                    mhs = new Mahasiswa(sks, jenis);
                    break;
            }

            // 3. Panggil method hitungBiayaKuliah() (Panggilan Polimorfik)
            // Java akan secara otomatis menjalankan rumus dari class yang benar (Reguler/Beasiswa/Internasional)
            double totalBiaya = mhs.hitungBiayaKuliah();

            // 4. Format dan Tampilkan biaya ke lblBiaya
            java.text.DecimalFormat formatter = new java.text.DecimalFormat("Rp #,###,###.00");
            lblBiaya.setText(formatter.format(totalBiaya));

        } catch (NumberFormatException e) {
            // Jika input SKS bukan angka
            lblBiaya.setText("SKS harus angka!");
        } catch (Exception e) {
            // Catch all untuk error lain
            lblBiaya.setText("Error hitung biaya: " + e.getMessage());
        }
    }

